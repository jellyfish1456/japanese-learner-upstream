// Coordinate transform helpers
export function toSVGCoords(
  lon: number, lat: number,
  minLon: number, minLat: number,
  scaleX: number, scaleY: number,
  offX: number, offY: number,
  height: number
): [number, number] {
  return [
    (lon - minLon) * scaleX + offX,
    height - (lat - minLat) * scaleY + offY,
  ];
}

export function polygonToPath(
  pts: [number, number][],
  minLon: number, minLat: number,
  scaleX: number, scaleY: number,
  offX: number, offY: number,
  height: number
): string {
  return pts
    .map(([lon, lat], i) => {
      const [x, y] = toSVGCoords(lon, lat, minLon, minLat, scaleX, scaleY, offX, offY, height);
      return `${i === 0 ? "M" : "L"}${x.toFixed(1)},${y.toFixed(1)}`;
    })
    .join(" ") + " Z";
}

